import Foundation
import UIKit

extension UIColor {
    static var STGray: UIColor {
        return UIColor(red: 102/255, green: 114/255, blue: 130/255, alpha: 1)
    }
    
    static var STBlack: UIColor {
        return UIColor(red: 19/255, green: 21/255, blue: 23/255, alpha: 1)
    }
}
